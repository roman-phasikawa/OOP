public class BitwiseOperator {
    public static void main(String[] args) {
        int a=2, b=6;
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);        //XOR
        System.out.println(~b);
        System.out.println(a>>2);       //Right Shift
        System.out.println(a<<4);       //Left Shift

    }
}
